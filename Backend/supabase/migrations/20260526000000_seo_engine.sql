-- ---------------------------------------------------------------------------
-- 2. TABLES
-- ---------------------------------------------------------------------------

-- SEO Keywords: The master keyword registry for the autonomous engine
CREATE TABLE IF NOT EXISTS seo_keywords (
  id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  keyword       TEXT NOT NULL,
  intent        seo_keyword_intent NOT NULL DEFAULT 'Informational',
  volume        INTEGER NOT NULL DEFAULT 0,
  cpc           NUMERIC(8,2) NOT NULL DEFAULT 0.00,
  pd            INTEGER NOT NULL DEFAULT 0,
  seo_difficulty INTEGER NOT NULL DEFAULT 0,
  cluster_parent TEXT,
  status        seo_keyword_status NOT NULL DEFAULT 'pending',
  created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- SEO Articles: Long-form content generated by the autonomous pipeline
CREATE TABLE IF NOT EXISTS seo_articles (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  keyword_id        UUID REFERENCES seo_keywords(id) ON DELETE SET NULL,
  title             TEXT NOT NULL,
  slug              TEXT NOT NULL UNIQUE,
  meta_title        TEXT NOT NULL,
  meta_description  TEXT NOT NULL,
  content_markdown  TEXT NOT NULL DEFAULT '',
  featured_image_url TEXT,
  status            seo_article_status NOT NULL DEFAULT 'draft',
  word_count        INTEGER NOT NULL DEFAULT 0,
  seo_score         NUMERIC(5,2) NOT NULL DEFAULT 0.00,
  human_score       NUMERIC(5,2) NOT NULL DEFAULT 0.00,
  schema_markup     JSONB DEFAULT '{}',
  published_at      TIMESTAMPTZ,
  created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Rankings Tracker: Historical SERP position snapshots
CREATE TABLE IF NOT EXISTS rankings_tracker (
  id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  keyword_id          UUID NOT NULL REFERENCES seo_keywords(id) ON DELETE CASCADE,
  google_rank         INTEGER NOT NULL DEFAULT 0,
  serps_snapshot_url  TEXT,
  tracked_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- SEO Settings: Global configuration for the autonomous orchestrator
CREATE TABLE IF NOT EXISTS seo_settings (
  id                      UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  default_meta_title      TEXT NOT NULL DEFAULT 'NobleInvoice - Premium Financial Management & CRM Platform',
  default_meta_description TEXT NOT NULL DEFAULT 'Streamline your professional workflow, collect payments rapidly with Flutterwave, and leverage smart client portals.',
  og_defaults             JSONB NOT NULL DEFAULT '{
    "og:type": "website",
    "og:site_name": "NobleInvoice",
