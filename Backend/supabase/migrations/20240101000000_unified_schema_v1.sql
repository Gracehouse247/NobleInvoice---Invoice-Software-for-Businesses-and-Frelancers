-- Profiles: Users manage own
CREATE POLICY "Users manage own profiles" ON public.profiles
  FOR ALL USING (auth.uid() = id);

-- Teams: Access via membership or ownership
CREATE POLICY "Team visibility" ON public.teams
  FOR SELECT USING (owner_id = auth.uid() OR check_team_access(id));

CREATE POLICY "Team management" ON public.teams
  FOR ALL USING (owner_id = auth.uid());

-- Team Members: Visibility for colleagues, management for owners/admins
CREATE POLICY "Member visibility" ON public.team_members
  FOR SELECT USING (check_team_access(team_id));

CREATE POLICY "Member management" ON public.team_members
  FOR ALL USING (check_team_access(team_id, ARRAY['owner', 'admin']::team_role[]));

-- Client/Invoice/Product/Expense: Team-based access
CREATE POLICY "Team client access" ON public.clients FOR ALL USING (check_team_access(team_id));
CREATE POLICY "Team invoice access" ON public.invoices FOR ALL USING (check_team_access(team_id));
CREATE POLICY "Team product access" ON public.products FOR ALL USING (check_team_access(team_id));
CREATE POLICY "Team expense access" ON public.expenses FOR ALL USING (check_team_access(team_id));
CREATE POLICY "Team recurring access" ON public.recurring_invoices FOR ALL USING (check_team_access(team_id));

-- Invoice Items: Access via parent invoice team
CREATE POLICY "Invoice items access" ON public.invoice_items
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM public.invoices 
      WHERE id = invoice_items.invoice_id 
      AND check_team_access(team_id)
    )
  );

-- 6. TRIGGERS

-- Auto-create profile
CREATE OR REPLACE FUNCTION public.handle_new_user() 
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, email, display_name)
  VALUES (new.id, new.email, new.raw_user_meta_data->>'full_name');
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

-- Updated At maintenance
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER set_profiles_updated_at BEFORE UPDATE ON public.profiles FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
CREATE TRIGGER set_teams_updated_at BEFORE UPDATE ON public.teams FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
CREATE TRIGGER set_clients_updated_at BEFORE UPDATE ON public.clients FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
CREATE TRIGGER set_products_updated_at BEFORE UPDATE ON public.products FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
CREATE TRIGGER set_invoices_updated_at BEFORE UPDATE ON public.invoices FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
