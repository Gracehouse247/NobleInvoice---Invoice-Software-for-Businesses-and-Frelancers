-- 4. SECURITY FUNCTIONS (RLS Helpers)

-- Check team access with role-based granularity
CREATE OR REPLACE FUNCTION public.check_team_access(
    t_id UUID, 
    required_roles team_role[] DEFAULT ARRAY['owner', 'admin', 'staff', 'accountant']::team_role[]
)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.team_members 
    WHERE team_id = t_id 
    AND user_id = auth.uid() 
    AND role = ANY(required_roles)
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

-- Check if user is the team owner (highest privilege)
CREATE OR REPLACE FUNCTION public.is_team_owner(t_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.teams
    WHERE id = t_id
    AND owner_id = auth.uid()
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

