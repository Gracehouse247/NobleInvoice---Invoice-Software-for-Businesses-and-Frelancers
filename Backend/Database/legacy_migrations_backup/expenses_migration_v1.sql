-- 1. Vendors Table (Suppliers you pay)
CREATE TABLE IF NOT EXISTS public.vendors (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  team_id UUID REFERENCES public.teams(id) ON DELETE CASCADE,
  user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  name TEXT NOT NULL,
  email TEXT,
  phone TEXT,
  category TEXT, -- e.g. 'Software', 'Rent', 'Hardware'
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- RLS for Vendors
ALTER TABLE vendors ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Team members can manage vendors" ON vendors;
CREATE POLICY "Team members can manage vendors" ON vendors FOR ALL USING (check_team_access(team_id, ARRAY['owner', 'admin', 'staff']::team_role[]));

-- 2. Expense Categories (Standard set)
-- We can treat categories as a TEXT column with a standard set in Flutter,
-- but a table allows for user-defined categories later.
CREATE TABLE IF NOT EXISTS public.expense_categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE, -- NULL for system-wide
  name TEXT NOT NULL,
  icon TEXT, -- Material icon name
  color TEXT, -- Hex color
  created_at TIMESTAMPTZ DEFAULT NOW()
);

