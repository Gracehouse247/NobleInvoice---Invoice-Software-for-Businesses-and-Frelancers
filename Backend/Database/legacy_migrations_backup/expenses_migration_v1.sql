  FOR ALL USING (user_id = auth.uid());

-- 3. Expenses Table
CREATE TABLE IF NOT EXISTS public.expenses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  team_id UUID REFERENCES public.teams(id) ON DELETE CASCADE,
  user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  vendor_id BIGINT REFERENCES public.vendors(id) ON DELETE SET NULL,
  category_id BIGINT REFERENCES public.expense_categories(id) ON DELETE SET NULL,
  amount NUMERIC(15,2) NOT NULL DEFAULT 0,
  currency_code TEXT DEFAULT 'USD',
  expense_date DATE NOT NULL DEFAULT CURRENT_DATE,
  receipt_url TEXT, -- URL from 'receipts' bucket
  notes TEXT,
  is_recurring BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- RLS for Expenses
ALTER TABLE expenses ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Team members can manage expenses" ON expenses;
CREATE POLICY "Team members can manage expenses" ON expenses FOR ALL USING (check_team_access(team_id, ARRAY['owner', 'admin', 'staff', 'accountant']::team_role[]));

-- 4. Receipts Storage Bucket
INSERT INTO storage.buckets (id, name, public) VALUES ('receipts', 'receipts', true)
ON CONFLICT (id) DO NOTHING;

-- Storage Policies
DROP POLICY IF EXISTS "Users can upload receipts" ON storage.objects;
DROP POLICY IF EXISTS "Anyone can view receipts" ON storage.objects;
DROP POLICY IF EXISTS "Users can delete own receipts" ON storage.objects;

CREATE POLICY "Users can upload receipts" ON storage.objects 
  FOR INSERT WITH CHECK (bucket_id = 'receipts' AND auth.uid()::text = (storage.foldername(name))[1]);
CREATE POLICY "Anyone can view receipts" ON storage.objects 
  FOR SELECT USING (bucket_id = 'receipts');
CREATE POLICY "Users can delete own receipts" ON storage.objects 
  FOR DELETE USING (bucket_id = 'receipts' AND auth.uid()::text = (storage.foldername(name))[1]);
